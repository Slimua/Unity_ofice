/**
 * An enumeration representing the relative date options for calculating a value to be used in date-based BooleanCriteria.
 */
export enum RelativeDate {
    RELATIVE_DATE_UNSPECIFIED, // Default value, do not use.
    PAST_YEAR,
    PAST_MONTH,
    PAST_WEEK,
    YESTERDAY,
    TODAY,
    TOMORROW,
}
